#include <iostream>
using std::cin;  // 앞으로 std::cin을 cin으로 부르겠다
using std::cout; // 앞으로 std::cout을 cout으로 부르겠다
using std::endl; // 앞으로 std::endl을 endl로 부르겠다

int main(void){
	int num = 20;
	cout << "Hello World!" << endl;
	cout << "Hello " << "World!" << endl;
	cout << num << ' ' << 'A';
	cout << ' ' << 3.14 << endl;
	return 0;
}